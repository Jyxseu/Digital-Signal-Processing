%fc=0.3KHz;delta=0.8dB;fr=0.2KHz;At=20dB;T=1ms;
%设计切比雪夫高通滤波器,只能使用双线性变换法
clear;clc;
fs=1000;%采样频率
fc=300;fr=200;
delta=0.8;At=20;
wc=2*fs*tan(2*pi*fc/(2*fs));
wr=2*fs*tan(2*pi*fr/(2*fs));
[N,wn]=cheb1ord(wc,wr,delta,At,'s');
[B,A]=cheby1(N,delta,wn,'high','s');
[num,den]=bilinear(B,A,fs);
[h,w]=freqz(num,den);
f=w/pi*500;
plot(f,20*log10(abs(h)));
axis([0,500,-60,0]);
grid;
xlabel('频率/Hz');
ylabel('幅度/dB');
title('切比雪夫高通数字滤波器');
